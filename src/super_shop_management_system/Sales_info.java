    public void showInfo()
    {
         try {
             rating=0;
             count=0;
             totalSale=0;
            
            String query = "SELECT *FROM salesinfo";
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection(url, username, password);

            Statement st = con.createStatement();
            result = st.executeQuery(query);
            while(result.next())
            {
               rating+=result.getInt("Rating");
               totalSale+=result.getInt("Total");
               count++;
            }
            avgRating=(float) (rating*1.0/count);
            avgSale=(float)(totalSale*1.0/count);
            salesL.setText(totalSale+"");
            ratingL.setText(avgRating+"");
            avgsL.setText(avgSale+"");
  
            st.close();
            con.close();

        } catch (Exception e)
        {
            System.out.println(e);
        }
    }
